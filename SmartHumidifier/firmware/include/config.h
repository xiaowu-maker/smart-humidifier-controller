/**
 * ============================================================
 * Smart Humidifier Controller (SHC) V1.0
 * 全局配置文件
 * ============================================================
 *
 * 说明：所有硬件引脚、控制参数、WiFi 凭证都在这里定义。
 *       换引脚、改阈值只需修改此文件，不需要动业务代码。
 *
 * 用法：#include "config.h"
 */

#ifndef CONFIG_H
#define CONFIG_H

// ============================================================
// 1. 项目信息
// ============================================================

/** 项目名称 */
#define PROJECT_NAME        "Smart Humidifier Controller"

/** 固件版本号（语义化版本） */
#define PROJECT_VERSION     "1.0.0"

/** 串口波特率 */
#define SERIAL_BAUDRATE     115200

// ============================================================
// 2. I2C 总线配置
// ============================================================

/**
 * I2C 数据线 (SDA)
 * ESP32 默认 I2C 引脚：GPIO21
 */
#define I2C_SDA_PIN         21

/**
 * I2C 时钟线 (SCL)
 * ESP32 默认 I2C 引脚：GPIO22
 */
#define I2C_SCL_PIN         22

/**
 * I2C 时钟频率 (Hz)
 * 100kHz = 标准模式，稳定可靠
 * 400kHz = 快速模式，线短时可用
 */
#define I2C_CLOCK_FREQ      100000

// ============================================================
// 3. AHT20 温湿度传感器
// ============================================================

/** AHT20 I2C 地址（7-bit） */
#define AHT20_I2C_ADDR      0x38

// ============================================================
// 4. OLED 显示屏 (SSD1306)
// ============================================================

/** SSD1306 I2C 地址（7-bit），常见为 0x3C */
#define OLED_I2C_ADDR       0x3C

/** OLED 屏幕宽度（像素） */
#define OLED_WIDTH          128

/** OLED 屏幕高度（像素） */
#define OLED_HEIGHT         64

// ============================================================
// 5. 继电器
// ============================================================

/**
 * 继电器控制引脚
 * GPIO4 = 安全引脚，上电默认低电平
 */
#define RELAY_PIN           4

/**
 * 继电器触发方式
 * false = 低电平触发（大多数继电器模块）
 * true  = 高电平触发
 */
#define RELAY_ACTIVE_HIGH   false

/** 继电器最短开启时间（毫秒），防止频繁启停损坏加湿器 */
#define RELAY_MIN_ON_TIME_MS    60000

/** 继电器最短关闭时间（毫秒），防止频繁启停 */
#define RELAY_MIN_OFF_TIME_MS   30000

// ============================================================
// 6. RGB LED（共阴极）
// ============================================================

/** 红色通道引脚 */
#define LED_RED_PIN         16

/** 绿色通道引脚 */
#define LED_GREEN_PIN       17

/** 蓝色通道引脚 */
#define LED_BLUE_PIN        18

/** LED PWM 频率 */
#define LED_PWM_FREQ        5000

/** LED PWM 分辨率（bit） */
#define LED_PWM_RESOLUTION  8

/** LED 正常亮度（0-255） */
#define LED_BRIGHTNESS      200

/** LED 夜间模式亮度 */
#define LED_BRIGHTNESS_NIGHT 50

// ============================================================
// 7. 蜂鸣器
// ============================================================

/** 蜂鸣器控制引脚 */
#define BUZZER_PIN          19

// ============================================================
// 8. 按钮
// ============================================================

/** 按钮 A 引脚（模式切换），内部上拉，按下为 LOW */
#define BUTTON_A_PIN        13

/** 按钮 B 引脚（恢复默认），内部上拉，按下为 LOW */
#define BUTTON_B_PIN        14

/** 按钮消抖时间（毫秒） */
#define BUTTON_DEBOUNCE_MS  30

/** 长按判定时间（毫秒） */
#define BUTTON_LONG_PRESS_MS    1000

/** 超长按判定时间（毫秒） */
#define BUTTON_VERY_LONG_PRESS_MS 3000

/** 双击判定间隔（毫秒） */
#define BUTTON_DOUBLE_CLICK_MS  400

// ============================================================
// 9. 控制参数
// ============================================================

/** 湿度下限阈值（%RH）：低于此值时开启加湿器 */
#define HUMIDITY_LOW_THRESHOLD      45.0f

/** 湿度上限阈值（%RH）：高于此值时关闭加湿器 */
#define HUMIDITY_HIGH_THRESHOLD     55.0f

/** 传感器读取间隔（毫秒） */
#define SENSOR_READ_INTERVAL_MS     2000

/** OLED 显示刷新间隔（毫秒） */
#define DISPLAY_UPDATE_INTERVAL_MS  2000

/**
 * 继电器防抖采样次数
 * 连续 N 次读数超阈值才改变继电器状态
 * 配合 SENSOR_READ_INTERVAL_MS=2000：
 *   3次 × 2秒 = 6秒防抖窗口
 */
#define RELAY_DEBOUNCE_COUNT        3

/** 传感器最大连续错误次数（超过后报警） */
#define SENSOR_MAX_ERRORS           3

/** OLED 屏幕保护超时（毫秒），10分钟无操作后降低刷新率 */
#define SCREEN_SAVER_TIMEOUT_MS     600000

// ============================================================
// 10. WiFi 配置（V2.0 使用）
// ============================================================

/** WiFi SSID */
#define WIFI_SSID           "YOUR_WIFI_SSID"

/** WiFi 密码 */
#define WIFI_PASSWORD       "YOUR_WIFI_PASSWORD"

/** WiFi 重连间隔（毫秒） */
#define WIFI_RECONNECT_INTERVAL_MS  30000

/** WiFi 连接超时（毫秒） */
#define WIFI_CONNECT_TIMEOUT_MS     15000

// ============================================================
// 11. Web 服务器配置（V2.0 使用）
// ============================================================

/** HTTP 服务器端口 */
#define WEB_SERVER_PORT     80

// ============================================================
// 12. 日志系统配置
// ============================================================

/**
 * 日志级别过滤
 * 0 = DEBUG（输出所有日志）
 * 1 = INFO
 * 2 = WARN
 * 3 = ERROR（仅输出错误）
 */
#define LOG_LEVEL           0

// ============================================================
// 13. 调试开关
// ============================================================

/** 启动时运行 GPIO 自检（调试用，正常运行时建议关闭） */
#define ENABLE_GPIO_SELF_TEST   false

/** 启用 I2C 总线扫描（启动时扫描所有 I2C 设备） */
#define ENABLE_I2C_SCAN         true

#endif // CONFIG_H
